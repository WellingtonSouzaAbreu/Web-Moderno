
let valor1 = 10
let valor2 = 5
let operacao = '/'
 
switch(operacao){
    case '+': {
        console.log(valor1 + valor2)
        break
    }
    case '-': {
        console.log(valor1 - valor2)
        break
    }
    case '*': {
        console.log(valor1 * valor2)
        break
    }
    case '/': {
        console.log(valor1 / valor2)
        break
    }
    default: console.log('Essa calculadora não é capaz de efetuar esse tipo de cálculo!')
}








